package com.arjunneup.wallet;

public class EducationModel {
    int education;
    String month;

    public EducationModel(int education, String month){
        this.education = education;
        this.month = month;
    }

    public void setEducation(int grocery){
        this.education = grocery;
    }
    public int getEducation(){
        return education;
    }

    public void setMonth(String month){
        this.month = month;
    }
    public String getMonth(){
        return month;
    }

}
